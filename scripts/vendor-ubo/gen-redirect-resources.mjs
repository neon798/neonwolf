// Generate UBORedirectResources.sys.mjs from uBO's redirect-resources.js
// metadata + web_accessible_resources payloads, both fetched at a pinned tag.
// Usage: node gen-redirect-resources.mjs <ubo-tag> <outfile>
import { writeFileSync, mkdtempSync } from 'node:fs';
import { tmpdir } from 'node:os';
import path from 'node:path';
import { pathToFileURL } from 'node:url';

const TAG = process.argv[2];
const OUT = process.argv[3];
if (!TAG || !OUT) {
  console.error('usage: node gen-redirect-resources.mjs <ubo-tag> <outfile>');
  process.exit(1);
}
const RAW = `https://raw.githubusercontent.com/gorhill/uBlock/${TAG}/src`;

// uBO redirect-engine.js mime map (by file extension).
const MIME = new Map([
  ['css', 'text/css'],
  ['gif', 'image/gif'],
  ['html', 'text/html'],
  ['js', 'text/javascript'],
  ['mp3', 'audio/mp3'],
  ['mp4', 'video/mp4'],
  ['png', 'image/png'],
  ['txt', 'text/plain'],
  ['xml', 'text/xml'],
]);

const metaSrc = await (await fetch(`${RAW}/js/redirect-resources.js`)).text();
const tmp = mkdtempSync(path.join(tmpdir(), 'ubo-rr-'));
const metaPath = path.join(tmp, 'redirect-resources.js');
writeFileSync(metaPath, metaSrc);
const meta = (await import(pathToFileURL(metaPath).href)).default;

const resources = {};
const aliases = {};
let skipped = [];
for (const [token, entry] of meta) {
  // click2load needs extension-UI template params; not servable natively.
  if (entry.params !== undefined) {
    skipped.push(token);
    continue;
  }
  const ext = token.includes('.') ? token.split('.').pop() : 'txt';
  const contentType = MIME.get(ext) || 'text/plain';
  const res = await fetch(`${RAW}/web_accessible_resources/${token}`);
  if (!res.ok) {
    skipped.push(token);
    continue;
  }
  const body = Buffer.from(await res.arrayBuffer());
  resources[token] = { contentType, base64: body.toString('base64') };
  const al = entry.alias === undefined ? []
    : Array.isArray(entry.alias) ? entry.alias : [entry.alias];
  for (const a of al) {
    aliases[a] = token;
  }
}

const out = `/* Neonwolf: uBlock Origin $redirect resources (neutered shims), vendored
 * from https://github.com/gorhill/uBlock at tag ${TAG}. GPL-3.0-or-later.
 * "powered by uBlock". Generated by scripts/vendor-ubo/build-redirect-resources.sh
 * -- do not edit by hand. Skipped (need extension UI): ${skipped.join(', ')} */
export default ${JSON.stringify({ version: TAG, resources, aliases }, null, 1)};
`;
writeFileSync(OUT, out);
console.log(`${Object.keys(resources).length} resources, ` +
  `${Object.keys(aliases).length} aliases, skipped: ${skipped.join(', ')}`);
